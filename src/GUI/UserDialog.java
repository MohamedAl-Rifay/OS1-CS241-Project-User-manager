    private void UpdateUser() {
        user.setHomeDir(User_HomeDir.getText())
                .setHomePhoneNumber(User_Home_Phone_Number.getText())
                .setID((int) User_UserID.getValue())
                .setName(User_Name.getText())
                .setOtherInformation(User_Password.getText())
                .setPhoneNumber(User_Phone_Number.getText())
                .setRoomNumber(User_Room_Number.getText())
                .setUsername(User_Username.getText())
                .setPassword(User_Password.getText())
                .setMinExpireDays((int) User_PE_Min.getValue())
                .setMaxExpireDays((int) User_PE_Max.getValue())
                .setWarningExpireDays((int) User_PE_Warn.getValue());
    }

