    private void Btn_OKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_OKActionPerformed
        String errors = "";

        if (User_Username.getText().equals("")) {
            errors += "Username, ";
        } else if (!editMood) {
            if (User_Name.getText().equals("")) {
                errors += "Name, ";
            } else if (User_Home_Phone_Number.getText().equals("")) {
                errors += "Home phone number, ";
            } else if (User_Password.getText().equals("")) {
                errors += "Password, ";
            } else if (User_Phone_Number.getText().equals("")) {
                errors += "Phone number, ";
            } else if (User_Room_Number.getText().equals("")) {
                errors += "Room Number";
            }
        }
        if (!"".equals(errors)) {
            JOptionPane.showMessageDialog(this, "Please, fill all required fields(" + errors + ").", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        SelectedOption = JOptionPane.OK_OPTION;
        UpdateUser();
        Close();
    }//GEN-LAST:event_Btn_OKActionPerformed

